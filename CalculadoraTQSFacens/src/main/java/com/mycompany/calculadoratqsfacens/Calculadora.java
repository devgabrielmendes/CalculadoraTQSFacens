package com.mycompany.calculadoratqsfacens;

public class Calculadora {
    
    //Recebe 2 números inteiros e retorna o resultado da soma entre eles.
    public int soma(int x, int y){
        
        return x + y;
    }

    //Recebe 2 números inteiros e retorna o resultado da subtração entre eles.
    public int subtracao(int x, int y){
        
        return x - y;
    }
    
    //Recebe 2 números inteiros e retorna o resultado da divisão entre eles.
    public int divisao(int x, int y){
        
        return x / y;
    }

    //Recebe 2 números inteiros e retorna o resultado da multiplicação entre eles.
    public int multiplicacao(int x, int y){
        
        return x * y;
    }
    
}
